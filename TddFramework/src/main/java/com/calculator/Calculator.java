package com.calculator;

public class Calculator {
    public Integer add(int a, int b) {
        return a + b;
    }

    public float divide(int a, int b) {
        return a / b;
    }

    public Integer multiply(int a, int b) {
        return a * b;
    }

    public Integer sub(int a, int b) {
        return a - b;
    }

}
